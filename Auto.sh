# Random banner picker
show_random_banner () {
  local banners=(onepiece skull spider dragon butterfly)
  local pick=${banners[RANDOM % ${#banners[@]}]}
  "banner_$pick" | gradient_print
}

# Tanggal
show_date () {
  local hari tgl bln thn
  hari=$(LC_TIME=id_ID.UTF-8 date +"%A" 2>/dev/null || date +"%A")
  tgl=$(date +"%d")
  bln=$(date +"%m")
  thn=$(date +"%Y")
  printf "\e[2;90m";figlet -f term -c -t "Powered by L4EROOR/KMB"
  echo -e "\n\033[1;91m        РЋ╝РЋљ\033[94mРўЙ \033[93mDATE\033[95m: \033[4;97m$hari\e[0m\033[94m , \033[4;92m$tgl\e[0m\033[95m-\033[4;92m$bln\e[0m\033[95m-\033[4;92m$thn\e[0m\033[94m Рўй\033[91mРЋљРЋЙ"
}

